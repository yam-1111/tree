const fs = require('fs');
const path = require('path');

const templatesDir = path.join(__dirname, '../templates');
const outputFile = path.join(__dirname, '../lib/templates-data.ts');

const files = fs.readdirSync(templatesDir);
const templates = {};

files.forEach(file => {
  if (file.endsWith('.tree')) {
    const name = file.replace('.tree', '');
    const content = fs.readFileSync(path.join(templatesDir, file), 'utf-8');
    templates[name] = content;
  }
});

const fileContent = `// Automatically generated by scripts/compile-templates.js. Do not edit directly!
export const TEMPLATES_DATA: Record<string, string> = ${JSON.stringify(templates, null, 2)};
`;

fs.writeFileSync(outputFile, fileContent, 'utf-8');
console.log('Successfully compiled templates to lib/templates-data.ts');
